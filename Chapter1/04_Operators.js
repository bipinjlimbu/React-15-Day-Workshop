//Arithmetic
console.log("Arithmetic Operators:")
let a = 4;
let b = 3;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b);

//Comparision
console.log("Comparision Operators:");
console.log("7 == '7' : "+(7 == "7"));
console.log("7 === '7' : "+(7 === "7"));
console.log("9 > 7 : "+(9 > 7));
console.log("9 < 7 : "+(9 < 7));
console.log("7 >= 7 : "+(7 >= 7));
console.log("9 <= 12 : "+(9 <= 12));

//Logical
console.log("Logical Operators:");
console.log("True and False : "+(true && false));
console.log("True or False : "+(true || false));
console.log("Not True : "+(!true));
